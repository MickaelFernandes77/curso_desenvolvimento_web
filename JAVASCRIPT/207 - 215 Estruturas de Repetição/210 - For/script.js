// sintaxe for:
// variavel: que sera usada na logica, para parada do laço
// condição: condição para iniciar a repetição
// controle: incremento da variável

// ++: incremento
// --: decremento
for (let i = 0; i <= 10; i++) {
  document.write(i + "<br>");
  // também posso usar o break, e o continue.
}

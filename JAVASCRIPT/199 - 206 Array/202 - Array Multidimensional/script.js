let lista_coisas = Array();

// atribuição de um array, ao indice frutas
lista_coisas['frutas'] = Array();

// inserindo valores nos indices dentro do array que está no indice frutas
lista_coisas['frutas'][0] = 'Banana';
lista_coisas['frutas'][1] = 'Maça';
lista_coisas['frutas'][2] = 'Goiaba';
lista_coisas['frutas'][3] = 'Laranja';

// atribuição de um array, ao indice pessoas, e já inserindo valores nos indices do array do indice pessoas
lista_coisas['pessoas'] = Array('Mickael', 'Fernando', 'Mário', 'José');
console.log(lista_coisas);
// acessando os valores do array multidimensional
console.log(lista_coisas['pessoas'][0]);

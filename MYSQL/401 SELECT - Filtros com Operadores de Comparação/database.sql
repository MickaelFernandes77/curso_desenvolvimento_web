-- Operador Igual(=)
SELECT nome, idade FROM tb_alunos WHERE interesse = 'Jogos';

-- operador menor(<)
SELECT nome, idade FROM tb_alunos WHERE idade < 25;

-- operador menor ou igual(<=)
SELECT nome, idade FROM tb_alunos WHERE idade <= 25;

-- operador maior(>)
SELECT nome, idade FROM tb_alunos WHERE idade > 35;

-- operador maior ou igual(>)
SELECT nome, idade FROM tb_alunos WHERE idade >= 35;
